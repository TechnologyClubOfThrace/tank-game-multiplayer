#ifndef COLLIDER2D_H
#define COLLIDER2D_H

enum class Collider2DType
{
    BaseClass,
    PointCollider2D,
    RectangleCollider2D,
    ConvexPolygonColider2D
};

enum class CollisionRespose
{
    NoResponse,
    RemoveEntity,
    CollideAndSlide,
    RevertTransform
};


class Collider2D
{
public:
    Collider2D();
    virtual ~Collider2D();

    const Collider2DType collider2DType;
    const CollisionRespose collisionResponse;

protected:
    Collider2D(const Collider2DType collider2d_type, const CollisionRespose collision_response);
};

#endif // COLLIDER2D_H
